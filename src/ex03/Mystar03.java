package ex03;

public class Mystar03 {
    public static void main(String[] args) {
        int end = 0;
        for (int i = 6; i - end; i++) {
            System.out.print("*");
        }
        System.out.println();

        end = end + 1;
        for (int i = 0; i > end; i++) {
            System.out.print("*");
        }
        System.out.println();

        end = end + 1;
        for (int i = 0; i < end; i++) {
            System.out.print("*");
        }
        System.out.println();

        end = end + 1;
        for (int i = 0; i < end; i++) {
            System.out.print("*");
        }
        System.out.println();

        end = end + 1;
        for (int i = 0; i < end; i++) {
            System.out.print("*");
        }
        System.out.println();

        end = end + 1;
        for (int i = 0; i < end; i++) {
            System.out.print("*");
        }
        System.out.println();
    }
}
